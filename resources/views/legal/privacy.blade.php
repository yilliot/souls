<h1>Circle @ Collective Privacy Policy</h1>
<p>This policy sets out how Circle @ Collective will handle personal information in accordance with the Data Protection Act 2010 in light of who we are and who we aspire to be as an organisation.</p>

<p>We respect your privacy and recognize that your personal data is important to you.</p>

<h3>Our Commitment</h3>
<p>Circle @ Collective is committed to protecting the privacy of our members and visitors. We value the trust of those we deal with, and of the public, and recognise that maintaining this trust requires that we be transparent and accountable in how we treat the personal information that you choose to share with us.</p>


<h3>What Personal Information Do We Collect?</h3>
<p>Personal information collected by Circle @ Collective may include: name, address, telephone number, email address and other contact information, date of birth, demographic information such as age and gender.</p>

<p>On occasion, we may also collect images of you or stories about you</p>

<p>We also use “cookies” to track visits to our website; you can usually disable cookies in your internet browser settings. You can find out more about cookies by looking at our <a href="/terms">Terms of Use</a>.</p>

<p>How Do We Collect Your Personal Information?</p>

<p>We collect most personal information directly from you. For example:</p>

<ul>
    <li>in forms or coupons filled out either online or in hard copy</li>
    <li>face to face</li>
    <li>via email</li>
    <li>via online surveys</li>
    <li>via social media messages or conversations</li>
    <li>during telephone conversations</li>
    <li>in voice or image recordings</li>
</ul>


<h3>Privacy Practices</h3>
<p>Personal information gathered by our organisation is kept in confidence. Our personnel are authorised to access personal information based only on their need to deal with it for the reason(s) for which it was obtained. Safeguards are in place to ensure that personal information is not disclosed or shared more widely than is necessary to achieve the purpose for which it was gathered. We contractually require any person or organisation providing services to Circle @ Collective to comply with privacy legislation. We also take measures to ensure that the integrity of personal information is maintained and retained only as long as it is required.</p>

<p>We collect, use and disclose personal information only for purposes that a reasonable person would consider appropriate in light of the circumstances. We do not rent or sell personal information to any other organisation.</p>

<h3>How Do We Use Your Personal Information?</h3>

<p>Circle @ Collective collects personal information in order to service your account, understand your preferences and provide you with relevant communications and materials.</p>



<h3>When Do We Disclose Your Personal Information?</h3>

<p>We do not disclose your personal information to other organisations except for our core business purposes; importantly, we don’t rent, sell or exchange our supporter information. We will never share your data with third parties for marketing purposes.
</p>

